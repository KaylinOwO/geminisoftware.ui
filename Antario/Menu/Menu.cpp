void Menu::HandleMenuVisibility(){
	static bool is_down = false;
	static bool is_clicked = false;

	if (Globals::PressedKeys[VK_INSERT])
	{
		is_clicked = false;
		is_down = true;
	}
	else if (!Globals::PressedKeys[VK_INSERT] && is_down)
	{
		is_clicked = true;
		is_down = false;
	}
	else
	{
		is_clicked = false;
		is_down = false;
	}

	if (is_clicked)
	{
		menuOpened = !menuOpened;
	}
}
